import Foundation

// MARK: - Array
extension Array {
    var isNotEmpty: Bool {
        return !isEmpty
    }
}

// MARK: - ArraySlice
extension ArraySlice {
    var isNotEmpty: Bool {
        return !isEmpty
    }
}

// MARK: - String
extension String {
    var isNotEmpty: Bool {
        return !isEmpty
    }
}
